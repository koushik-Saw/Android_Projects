package com.example.schoolproject.model;

public class ClassNames {

    String mclassname;
    int mclassimages;

    public ClassNames(String mclassname, int mclassimages) {
        this.mclassname = mclassname;
        this.mclassimages = mclassimages;
    }

    public String getMclassname() {
        return mclassname;
    }

    public int getMclassimages() {
        return mclassimages;
    }
}
